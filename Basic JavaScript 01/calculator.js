var first_number;
first_number=45;
var second_number;
second_number=46;
var symbol;
symbol='*';
switch(symbol){
        case'+':
        console.log(first_number+second_number);
        break;
        case'*':
        console.log(first_number*second_number);
        break;
        case'/':
        console.log(first_number/second_number);
        break;
        case'-':
        console.log(first_number-second_number);
        break;
        case'%':
        console.log(first_number%second_number);
        break;

}